package model;

public class Amount {

	private double amountNumber;
	private String coin;
	
	
	public double getAmountNumber() {
		return amountNumber; }
	public void setAmountNumber(double amountNumber) {
		this.amountNumber = amountNumber; }
	
	
	public String getCoin() {
		return coin; }
	public void setCoin(String coin) {
		this.coin = coin; }	
	
}
